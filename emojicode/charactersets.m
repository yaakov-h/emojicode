void AddCharactersInStringToMutableArray(NSMutableArray * array, const NSString * const string)
{
	NSUInteger index = 0;
	while (index < string.length)
	{
		NSRange nextCharacterRange = [string rangeOfComposedCharacterSequenceAtIndex:index];
		NSString * nextCharacter = [string substringWithRange:nextCharacterRange];
		[array addObject:nextCharacter];
		index += nextCharacterRange.length;
	}
}

